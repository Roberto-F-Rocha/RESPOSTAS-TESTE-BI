--> 1. Liste os candidatos que possuem habilidades que aparecem mais de uma vez na tabela de habilidades.
        Objetivo: Mostrar o nome do candidato, a habilidade duplicada e a quantidade de vezes que ela aparece.
        Detalhes: Precisamos contar as habilidades duplicadas de cada candidato.
        
--> 2. Retorne os candidatos que possuem pelo menos uma habilidade em comum com outro candidato.
        Objetivo: Encontrar candidatos que compartilham pelo menos uma habilidade com outro candidato.
        Detalhes: Não considerar a mesma habilidade para o mesmo candidato, ou seja, comparar candidatos diferentes com habilidades em comum.

--> 3. Encontre os candidatos que têm a maior quantidade de habilidades de todos os candidatos.
        Objetivo: Listar os candidatos com o maior número de habilidades.
        Detalhes: Caso haja empate, todos os candidatos empatados devem ser mostrados.
        Essas questões envolvem conceitos mais avançados de SQL, como subconsultas, junções complexas e agregações.

--> 0000. Bônus: Liste os candidatos que possuem mais habilidades do que a média de habilidades de todos os candidatos.

